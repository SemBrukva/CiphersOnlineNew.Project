    /**
     * Вычисляет χ²-отклонение текста от ожидаемого распределения частот языка.
     *
     * Учитываются только буквы, входящие в частотную таблицу данного алфавита.
     * Если в тексте нет ни одной подходящей буквы, возвращает очень большое число.
     */
    public function chiSquared(string $text, string $alphabet): float
    {
        $freqs = self::FREQUENCIES[$alphabet] ?? null;
        if ($freqs === null) {
            return 0.0;
        }

        $counts = [];
        $total  = 0;
        $length = mb_strlen($text);

        for ($i = 0; $i < $length; $i++) {
            $char = mb_strtolower(mb_substr($text, $i, 1));
            if (isset($freqs[$char])) {
                $counts[$char] = ($counts[$char] ?? 0) + 1;
                $total++;
            }
        }

        if ($total === 0) {
            return 1.0e9;
        }

        $chi = 0.0;
        foreach ($freqs as $letter => $expectedPct) {
            $observed = ($counts[$letter] ?? 0) / $total;
            $expected = $expectedPct / 100.0;
            $chi     += (($observed - $expected) ** 2) / $expected;
        }

        return $chi;
    }

    /**
     * Подсчитывает количество букв алфавита в тексте (без учёта регистра).
     */
    public function countLetters(string $text, string $alphabet): int
    {
        $freqs  = self::FREQUENCIES[$alphabet] ?? [];
        $total  = 0;
        $length = mb_strlen($text);

        for ($i = 0; $i < $length; $i++) {
            $char = mb_strtolower(mb_substr($text, $i, 1));
            if (isset($freqs[$char])) {
                $total++;
            }
        }

        return $total;
    }

    /**
     * Буквы, уникальные для конкретного латинского алфавита: при наличии любой
     * из них в тексте язык-кандидат получает приоритет над дефолтным 'en'.
     *
     * Разделение en / es / fr / it / de / pt / tr на основе одного только подсчёта
     * совпадений букв даёт шум: эти алфавиты пересекаются на 23+ буквах. Уникальные
     * акцентные/специальные символы — единственный надёжный сигнал на коротком
     * шифр-тексте.
     *
     * @var array<string, string[]>
     */
    private const array LATIN_EXCLUSIVE_MARKS = [
        'es' => ['ñ'],
        'de' => ['ß', 'ä', 'ö', 'ü'],
        'tr' => ['ı', 'ş', 'ğ'],
        'pt' => ['ã', 'õ', 'á', 'í', 'ú'],
        'fr' => ['ë', 'œ', 'â', 'ô', 'ÿ', 'ê', 'è', 'ï', 'î', 'ù', 'û', 'à'],
    ];

    /**
     * Определяет наиболее вероятный язык текста.
     *
     * Двухстадийный алгоритм:
     * 1. Если кириллица доминирует среди букв (≥ 50% от общего числа букв) → 'ru'.
     *    Доли ниже игнорируются: единичные русские слова в латинском тексте не
     *    должны его «угнать».
     * 2. Для латинского текста ищем уникальные для конкретного языка символы
     *    (ñ → es, ß/ä/ö/ü → de, ı/ş/ğ → tr и т.п.); язык с наибольшим числом
     *    совпадений побеждает.
     * 3. Если уникальных символов нет — отдаём 'en' как дефолт (en/it отличить
     *    без акцентов нельзя, так что выбор английского — разумный baseline).
     */
    public function detectAlphabet(string $text): string
    {
        $cyrillicCount = $this->countLetters($text, 'ru');
        $totalLetters  = preg_match_all('/\p{L}/u', $text) ?: 0;
        if ($totalLetters > 0 && $cyrillicCount / $totalLetters >= 0.5) {
            return 'ru';
        }

        $lower     = mb_strtolower($text);
        $bestCount = 0;
        $best      = 'en';
        foreach (self::LATIN_EXCLUSIVE_MARKS as $code => $marks) {
            $count = 0;
            foreach ($marks as $mark) {
                $count += mb_substr_count($lower, $mark);
            }
            if ($count > $bestCount) {
                $bestCount = $count;
                $best      = $code;
            }
        }

        return $best;
    }

    /**
     * Возвращает ожидаемые частоты букв алфавита в порядке индексов алфавита (доли 0..1).
     *
     * Буквы, отсутствующие в частотной таблице, получают 0. Используется в брут-форсе
     * для одностадийного χ² по гистограмме шифртекста без посимвольного обхода.
     *
     * @param  string[] $alphabetData Алфавит в порядке индексов.
     * @return float[]                Ожидаемые доли, индексированные по позиции в алфавите.
     */
    public function expectedFrequencyVector(string $alphabet, array $alphabetData): array
    {
        $freqs  = self::FREQUENCIES[$alphabet] ?? [];
        $result = array_fill(0, count($alphabetData), 0.0);
        foreach ($alphabetData as $idx => $letter) {
            if (isset($freqs[$letter])) {
                $result[$idx] = $freqs[$letter] / 100.0;
            }
        }

        return $result;
    }

    /**
     * Возвращает теоретический индекс совпадений (IC) для естественного языка.
     *
     * Вычисляется как ∑p_i² по ожидаемым частотам алфавита.
     * Применяется в IC-анализе при взломе шифра Виженера.
     */
    public function naturalIc(string $alphabet): float
    {
        $freqs = self::FREQUENCIES[$alphabet] ?? null;
        if ($freqs === null) {
            return 0.065;
        }

        $ic = 0.0;
        foreach ($freqs as $pct) {
            $p   = $pct / 100.0;
            $ic += $p * $p;
        }

        return $ic;
    }

    /**
     * Преобразует массив χ²-значений в оценки пригодности от 0 до 100.
     * Лучший вариант (минимальный χ²) получает 100, остальные — пропорционально меньше.
     *
     * @param  float[] $chiValues Индексированный массив χ²-значений.
     * @return int[]              Оценки пригодности в том же порядке.
     */
    public function toFitness(array $chiValues): array
    {
        if ($chiValues === []) {
            return [];
        }

        $inverted = array_map(static fn (float $chi): float => 1.0 / (1.0 + $chi), $chiValues);
        $max      = max($inverted) ?: 1.0;

        return array_map(static fn (float $v): int => (int) round(($v / $max) * 100), $inverted);
    }
